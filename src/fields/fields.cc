string
FieldUtils::getFieldName (int f)
{
    map<int, string>::const_iterator itr;
    itr = FieldUtils::mFieldNames.find (f);
    
    if (itr == FieldUtils::mFieldNames.end ())
    {
        stringstream ss;
        ss << f;
        return ss.str ();
    }
    else
        return itr->second;
}   

